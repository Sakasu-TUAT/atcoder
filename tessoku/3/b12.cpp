double binary_search(ll n){
  double ng=0, ok = 100;
  auto f = [](double x) -> double { return (x*x*x)+x; };
  while(true){
    double mid = (ok+ng)/2;
    double fx = f(mid);
    if(abs(f(mid)-n)<=0.001) return mid;
    else if(n>fx) {ng=mid;}
    else ok = mid;
  }
}
